export async function exportCompletedAnalysis(options: {
  baseName: string;
  title: string;
  data: Record<string, unknown>;
  chartContainer?: HTMLElement | null;
  chartSubtitle?: string;
}) {
  const safeBase = makeSafeFilename(options.baseName);
  exportJSON(`${safeBase}-result.json`, options.data);
  exportResultCSV(`${safeBase}-result.csv`, options.data);
  exportHTML(`${safeBase}-report.html`, options.title, options.data);
  exportPDF(`${safeBase}-report.pdf`, options.title, options.data);
  exportChartSVG(`${safeBase}-chart.svg`, options.chartContainer ?? null, options.title, options.chartSubtitle);
  await exportChartPNG(`${safeBase}-chart.png`, options.chartContainer ?? null, options.title, options.chartSubtitle);
  await exportChartJPG(`${safeBase}-chart.jpg`, options.chartContainer ?? null, options.title, options.chartSubtitle);
}


export function exportSectionBundle(options: {
  baseName: string;
  title: string;
  data: Record<string, unknown>;
  csvRows?: Record<string, unknown>[];
}) {
  const safeBase = makeSafeFilename(options.baseName);
  exportJSON(`${safeBase}.json`, options.data);
  exportCSV(`${safeBase}.csv`, options.csvRows ?? Object.entries(options.data).map(([metric, value]) => ({ metric, value: stringifyCell(value) })));
  exportHTML(`${safeBase}.html`, options.title, options.data);
  exportPDF(`${safeBase}.pdf`, options.title, options.data);
}
